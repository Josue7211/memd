//! A5 report emitters — NDJSON per scenario + markdown section appended
//! to `docs/verification/SUBSTRATE_BENCHMARKS.md`.
//!
//! Output shape per `phase-a5-plan.md` §2:
//!   `{"suite":"…","run_id":"…","ts_ms":…,"seed":…,"fact_count":…,
//!    "cut_k":…,"recall_at_1":…,"recall_at_3":…,"answer_exact_match":…,
//!    "tokens_per_recall":…,"latency_ms_p50":…,"latency_ms_p95":…,"pass":…}`

use serde::{Deserialize, Serialize};
use std::path::Path;

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub(crate) struct ScenarioRecord {
    pub(crate) suite: String,
    pub(crate) run_id: String,
    pub(crate) ts_ms: i64,
    pub(crate) seed: u64,
    pub(crate) fact_count: usize,
    pub(crate) cut_k: usize,
    pub(crate) recall_at_1: f64,
    pub(crate) recall_at_3: f64,
    pub(crate) answer_exact_match: f64,
    pub(crate) tokens_per_recall: u64,
    pub(crate) latency_ms_p50: u64,
    pub(crate) latency_ms_p95: u64,
    pub(crate) pass: bool,
}

/// Append `records` to `path` in NDJSON form (one record per line).
/// Creates the parent dir on first write.
pub(crate) fn append_ndjson(path: &Path, records: &[ScenarioRecord]) -> std::io::Result<()> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)?;
    }
    let mut buf = String::new();
    for r in records {
        let line = serde_json::to_string(r).map_err(|e| {
            std::io::Error::new(std::io::ErrorKind::InvalidData, e.to_string())
        })?;
        buf.push_str(&line);
        buf.push('\n');
    }
    use std::io::Write;
    let mut f = std::fs::OpenOptions::new()
        .create(true)
        .append(true)
        .open(path)?;
    f.write_all(buf.as_bytes())
}

/// Magic markers used to keep one block per suite in
/// `SUBSTRATE_BENCHMARKS.md`. Re-running the bench replaces the block
/// in-place rather than appending a duplicate.
fn section_open(suite: &str) -> String {
    format!("<!-- substrate:{suite}:open -->")
}
fn section_close(suite: &str) -> String {
    format!("<!-- substrate:{suite}:close -->")
}

const DOC_HEADER: &str = "# Substrate Benchmarks\n\n\
> Auto-generated by `memd benchmark substrate`. Each section between\n\
> `<!-- substrate:<suite>:open -->` and `<!-- substrate:<suite>:close -->`\n\
> markers is regenerated on every run; edits inside a block will be\n\
> overwritten. Add prose outside the markers if you need permanence.\n\n";

/// Render the markdown body for a suite from a slice of records.
pub(crate) fn render_section(suite: &str, records: &[ScenarioRecord]) -> String {
    let mut out = String::new();
    out.push_str(&section_open(suite));
    out.push('\n');
    out.push_str(&format!("\n## {suite}\n\n"));
    if records.is_empty() {
        out.push_str("_(no runs recorded)_\n\n");
    } else {
        out.push_str("| seed | facts | cut_k | recall@1 | recall@3 | exact_match | p50 ms | p95 ms | pass |\n");
        out.push_str("| ---:| ---:| ---:| ---:| ---:| ---:| ---:| ---:|:---:|\n");
        for r in records {
            out.push_str(&format!(
                "| {seed} | {facts} | {cut} | {r1:.3} | {r3:.3} | {em:.3} | {p50} | {p95} | {pass} |\n",
                seed = r.seed,
                facts = r.fact_count,
                cut = r.cut_k,
                r1 = r.recall_at_1,
                r3 = r.recall_at_3,
                em = r.answer_exact_match,
                p50 = r.latency_ms_p50,
                p95 = r.latency_ms_p95,
                pass = if r.pass { "✓" } else { "✗" },
            ));
        }
        out.push('\n');
    }
    out.push_str(&section_close(suite));
    out.push('\n');
    out
}

/// Append-or-replace the suite's section in `doc_path`. Creates the
/// file with `DOC_HEADER` if absent.
pub(crate) fn upsert_markdown_section(
    doc_path: &Path,
    suite: &str,
    records: &[ScenarioRecord],
) -> std::io::Result<()> {
    if let Some(parent) = doc_path.parent() {
        std::fs::create_dir_all(parent)?;
    }
    let existing = std::fs::read_to_string(doc_path).unwrap_or_else(|_| DOC_HEADER.to_string());
    let header_present = existing.contains("# Substrate Benchmarks");
    let mut new = if header_present {
        existing
    } else {
        format!("{DOC_HEADER}{existing}")
    };

    let open = section_open(suite);
    let close = section_close(suite);
    let block = render_section(suite, records);

    if let (Some(start), Some(end_rel)) = (new.find(&open), new[new.find(&open).unwrap_or(0)..].find(&close)) {
        let end = new.find(&open).unwrap() + end_rel + close.len();
        // Drop trailing newline immediately after close marker too, so
        // re-runs don't grow the file by one blank line each time.
        let mut tail_drop = end;
        if new.as_bytes().get(tail_drop) == Some(&b'\n') {
            tail_drop += 1;
        }
        let _ = start; // shadowed; recompute via splice below
        let start = new.find(&open).unwrap();
        new.replace_range(start..tail_drop, "");
        new.insert_str(start, &format!("{block}\n"));
    } else {
        // Append.
        if !new.ends_with('\n') {
            new.push('\n');
        }
        new.push_str(&block);
        new.push('\n');
    }

    std::fs::write(doc_path, new)
}

#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::tempdir;

    fn sample_record(seed: u64, cut_k: usize, recall: f64) -> ScenarioRecord {
        ScenarioRecord {
            suite: "cross-session-recall".into(),
            run_id: format!("run-{seed}-{cut_k}"),
            ts_ms: 1_700_000_000_000,
            seed,
            fact_count: 50,
            cut_k,
            recall_at_1: recall,
            recall_at_3: (recall + 0.05).min(1.0),
            answer_exact_match: recall,
            tokens_per_recall: 220,
            latency_ms_p50: 22,
            latency_ms_p95: 61,
            pass: recall >= 0.80,
        }
    }

    /// Test 8 — `report_emits_valid_ndjson_per_scenario`.
    /// Each line of the output file must round-trip as a single
    /// `ScenarioRecord` and the file must end with `\n`.
    #[test]
    fn report_emits_valid_ndjson_per_scenario() {
        let dir = tempdir().unwrap();
        let path = dir.path().join("results.ndjson");
        let recs = vec![
            sample_record(42, 2, 0.91),
            sample_record(42, 4, 0.87),
            sample_record(42, 8, 0.79),
        ];
        append_ndjson(&path, &recs).unwrap();

        let body = std::fs::read_to_string(&path).unwrap();
        assert!(body.ends_with('\n'));
        let mut count = 0;
        for line in body.lines() {
            let parsed: ScenarioRecord = serde_json::from_str(line)
                .unwrap_or_else(|e| panic!("invalid ndjson line {line:?}: {e}"));
            assert_eq!(parsed.suite, "cross-session-recall");
            count += 1;
        }
        assert_eq!(count, recs.len());

        // Second append must add lines, not overwrite.
        append_ndjson(&path, &[sample_record(43, 2, 0.95)]).unwrap();
        let body = std::fs::read_to_string(&path).unwrap();
        assert_eq!(body.lines().count(), recs.len() + 1);
    }

    /// Test 9 — `report_appends_markdown_section_to_substrate_doc`.
    /// First call creates the file with header + suite block. Second
    /// call replaces the suite block in-place (idempotent).
    #[test]
    fn report_appends_markdown_section_to_substrate_doc() {
        let dir = tempdir().unwrap();
        let doc = dir.path().join("SUBSTRATE_BENCHMARKS.md");

        // First run.
        let recs_v1 = vec![sample_record(42, 2, 0.91)];
        upsert_markdown_section(&doc, "cross-session-recall", &recs_v1).unwrap();
        let body = std::fs::read_to_string(&doc).unwrap();
        assert!(body.starts_with("# Substrate Benchmarks"), "header missing");
        assert!(body.contains("<!-- substrate:cross-session-recall:open -->"));
        assert!(body.contains("<!-- substrate:cross-session-recall:close -->"));
        assert!(body.contains("0.910"), "recall row missing");

        // Second run with different numbers — must replace, not duplicate.
        let recs_v2 = vec![sample_record(42, 2, 0.97)];
        upsert_markdown_section(&doc, "cross-session-recall", &recs_v2).unwrap();
        let body = std::fs::read_to_string(&doc).unwrap();
        assert_eq!(
            body.matches("<!-- substrate:cross-session-recall:open -->").count(),
            1,
            "duplicate suite block after re-run"
        );
        assert!(body.contains("0.970"));
        assert!(!body.contains("0.910"), "stale row left behind");

        // Adding a different suite must coexist with the first.
        let other = vec![sample_record(7, 1, 0.50)];
        upsert_markdown_section(&doc, "correction-propagation", &other).unwrap();
        let body = std::fs::read_to_string(&doc).unwrap();
        assert!(body.contains("<!-- substrate:cross-session-recall:open -->"));
        assert!(body.contains("<!-- substrate:correction-propagation:open -->"));
    }
}
