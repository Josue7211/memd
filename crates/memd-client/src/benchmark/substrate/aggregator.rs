//! G5 aggregator — suite-of-suites runner + canonical
//! `SUBSTRATE_BENCHMARKS.md` regenerator.
//!
//! Suites run in the fixed order pinned by V5-INTEGRATION §5b.2
//! (A5 → B5 → C5 → D5 → E5 → F5 → G5). Each per-suite call returns a
//! `SuiteSummary { id, pass, metrics, fail_reason }`. The regenerator then
//! emits one block per suite plus a composite + history footer.
//!
//! Strict-mode regeneration rules (V5-INTEGRATION §9b) live in
//! `ten_star_writer.rs`; this module only concerns the suites doc.

