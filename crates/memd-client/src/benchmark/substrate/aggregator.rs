fn run_f5(opts: &AggregatorOptions) -> SuiteSummary {
    let cfg = F5RunConfig::default_with_results_dir(opts.results_dir.clone());
    let _ = opts.seed;
    match run_f5_in_process(&cfg) {
        Ok(o) => {
            let mut m = BTreeMap::new();
            m.insert("queries".into(), o.records.len() as f64);
            let correct = o.records.iter().filter(|r| r.correct_at_1).count();
            let rate = if o.records.is_empty() { 0.0 } else { correct as f64 / o.records.len() as f64 };
            m.insert("correct_type_rate_at_1".into(), rate);
            if o.overall_pass {
                SuiteSummary::passed("typed-retrieval", m)
            } else {
                SuiteSummary::failed("typed-retrieval", "pass-gate failed", m)
            }
        }
        Err(e) => SuiteSummary::failed("typed-retrieval", &e.to_string(), BTreeMap::new()),
    }
}

