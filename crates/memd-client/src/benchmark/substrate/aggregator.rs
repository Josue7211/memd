/// Canonical execution order. Mirrors V5-INTEGRATION §5b.2.
pub(crate) const SUITE_ORDER: &[&str] = &[
    "cross-session-recall",
    "correction-propagation",
    "cross-harness",
    "progressive-depth",
    "provenance-integrity",
    "typed-retrieval",
    "adversarial-noise",
];

/// Per-suite aggregate summary captured by `--all` runs.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub(crate) struct SuiteSummary {
    pub(crate) id: String,
    pub(crate) pass: bool,
    pub(crate) metrics: BTreeMap<String, f64>,
    pub(crate) skipped: bool,
    pub(crate) fail_reason: Option<String>,
}

impl SuiteSummary {
    pub(crate) fn passed(id: &str, metrics: BTreeMap<String, f64>) -> Self {
        Self { id: id.into(), pass: true, metrics, skipped: false, fail_reason: None }
    }
    pub(crate) fn failed(id: &str, reason: &str, metrics: BTreeMap<String, f64>) -> Self {
        Self {
            id: id.into(),
            pass: false,
            metrics,
            skipped: false,
            fail_reason: Some(reason.into()),
        }
    }
    pub(crate) fn skipped(id: &str, reason: &str) -> Self {
        Self {
            id: id.into(),
            pass: false,
            metrics: BTreeMap::new(),
            skipped: true,
            fail_reason: Some(reason.into()),
        }
    }
}

const DOC_HEADER: &str = "# memd Substrate Benchmarks\n\n\
> Regenerated by `memd benchmark substrate --all --regenerate-report`. Do\n\
> not hand-edit. Each section between `<!-- substrate-agg:<suite>:open -->`\n\
> and `<!-- substrate-agg:<suite>:close -->` markers is rewritten on every\n\
> run.\n\n";

fn agg_open(suite: &str) -> String { format!("<!-- substrate-agg:{suite}:open -->") }
fn agg_close(suite: &str) -> String { format!("<!-- substrate-agg:{suite}:close -->") }

/// Render the per-suite summary section.
fn render_suite_block(s: &SuiteSummary) -> String {
    let mut out = String::new();
    out.push_str(&agg_open(&s.id));
    out.push('\n');
    out.push_str(&format!("\n### {}\n\n", s.id));
    let status = if s.skipped { "skipped" } else if s.pass { "pass" } else { "fail" };
    out.push_str(&format!("**status:** {status}\n\n"));
    if let Some(reason) = &s.fail_reason {
        out.push_str(&format!("**reason:** {reason}\n\n"));
    }
    if !s.metrics.is_empty() {
        out.push_str("| metric | value |\n| --- | ---:|\n");
        for (k, v) in &s.metrics {
            out.push_str(&format!("| {} | {:.3} |\n", k, v));
        }
        out.push('\n');
    }
    out.push_str(&agg_close(&s.id));
    out.push('\n');
    out
}

/// Render the composite table from per-suite pass/fail status.
fn render_composite(summaries: &[SuiteSummary]) -> String {
    let mut out = String::new();
    out.push_str("## Composite\n\n");
    out.push_str("| suite | status |\n| --- | :---:|\n");
    for s in summaries {
        let status = if s.skipped { "—" } else if s.pass { "✓" } else { "✗" };
        out.push_str(&format!("| {} | {} |\n", s.id, status));
    }
    let pass_count = summaries.iter().filter(|s| s.pass).count();
    let total = summaries.iter().filter(|s| !s.skipped).count();
    out.push_str(&format!("\n**suites passing:** {pass_count}/{total}\n\n"));
    out
}

/// Append a one-line history entry (date → pass count). Idempotent under
/// re-runs on the same date — appends a new row each invocation.
fn render_history_entry(summaries: &[SuiteSummary]) -> String {
    let date = Utc::now().format("%Y-%m-%d").to_string();
    let pass = summaries.iter().filter(|s| s.pass).count();
    let total = summaries.iter().filter(|s| !s.skipped).count();
    format!("- {date}: {pass}/{total} suites passing\n")
}

/// Regenerate the canonical SUBSTRATE_BENCHMARKS.md from a slice of suite
/// summaries. Replaces every `<!-- substrate-agg:*:* -->` block in place;
/// preserves any prose outside the markers and the History section.
pub(crate) fn regenerate_substrate_benchmarks_md(
    doc_path: &Path,
    summaries: &[SuiteSummary],
) -> std::io::Result<()> {
    if let Some(parent) = doc_path.parent() {
        std::fs::create_dir_all(parent)?;
    }

    // Always start from the canonical header so the file stays well-shaped.
    let mut body = String::from(DOC_HEADER);
    body.push_str(&render_composite(summaries));
    body.push_str("## Suites\n\n");
    for s in summaries {
        body.push_str(&render_suite_block(s));
        body.push('\n');
    }

    // History: read existing entries from prior file (if any) and append one.
    let mut history = String::from("## History\n\n");
    if let Ok(prior) = std::fs::read_to_string(doc_path) {
        if let Some(idx) = prior.find("## History") {
            let tail = &prior[idx + "## History".len()..];
            for line in tail.lines() {
                let trim = line.trim();
                if trim.starts_with("- ") {
                    history.push_str(line);
                    history.push('\n');
                }
            }
        }
    }
    history.push_str(&render_history_entry(summaries));
    body.push_str(&history);

    std::fs::write(doc_path, body)
}

/// Driver flags for the suite-of-suites runner. Mirrors the new
/// `--fail-fast`, `--regenerate-report`, `--allow-below-target` flags on
/// `SubstrateArgs`.
#[derive(Debug, Clone)]
pub(crate) struct AggregatorOptions {
    pub(crate) results_dir: PathBuf,
    pub(crate) seed: Option<u64>,
    pub(crate) fail_fast: bool,
}

impl AggregatorOptions {
    pub(crate) fn with_results_dir(results_dir: PathBuf) -> Self {
        Self { results_dir, seed: None, fail_fast: false }
    }
}

/// Run every registered substrate suite in the canonical SUITE_ORDER.
/// Each suite's in-process runner produces a `SuiteSummary`. With
/// `fail_fast = true`, halts on the first failing suite and marks the
/// remainder as `skipped("fail-fast halt")`.
///
/// Returns the collected summaries in SUITE_ORDER. Suite-runner I/O
/// errors are surfaced as `failed` summaries, never panics — so the
/// regenerator always has an entry per suite to render.
pub(crate) fn run_aggregator(opts: &AggregatorOptions) -> Vec<SuiteSummary> {
    let mut out = Vec::with_capacity(SUITE_ORDER.len());
    let mut halted = false;

    for suite in SUITE_ORDER {
        if halted {
            out.push(SuiteSummary::skipped(suite, "fail-fast halt"));
            continue;
        }
        let summary = match *suite {
            "cross-session-recall" => run_a5(opts),
            "correction-propagation" => run_b5(opts),
            "cross-harness" => run_c5(opts),
            "progressive-depth" => run_d5(opts),
            "provenance-integrity" => run_e5(opts),
            "typed-retrieval" => run_f5(opts),
            "adversarial-noise" => run_g5(opts),
            other => SuiteSummary::failed(other, "unregistered suite", BTreeMap::new()),
        };
        let pass = summary.pass;
        out.push(summary);
        if !pass && opts.fail_fast {
            halted = true;
        }
    }
    out
}

fn run_a5(opts: &AggregatorOptions) -> SuiteSummary {
    let mut cfg = A5RunConfig::default_with_results_dir(opts.results_dir.clone());
    if let Some(s) = opts.seed { cfg.seed = s; }
    match run_a5_in_process(&cfg) {
        Ok(o) => {
            let mut m = BTreeMap::new();
            m.insert("scenarios".into(), o.records.len() as f64);
            if o.overall_pass {
                SuiteSummary::passed("cross-session-recall", m)
            } else {
                SuiteSummary::failed("cross-session-recall", "pass-gate failed", m)
            }
        }
        Err(e) => SuiteSummary::failed("cross-session-recall", &e.to_string(), BTreeMap::new()),
    }
}

