#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::tempdir;

    fn pass_summary(id: &str) -> SuiteSummary {
        let mut m = BTreeMap::new();
        m.insert("pass".to_string(), 1.0);
        SuiteSummary::passed(id, m)
    }

    #[test]
    fn suite_order_lists_all_seven_suites() {
        assert_eq!(SUITE_ORDER.len(), 7);
        assert_eq!(SUITE_ORDER[0], "cross-session-recall");
        assert_eq!(SUITE_ORDER[6], "adversarial-noise");
    }

    #[test]
    fn regenerator_writes_one_block_per_suite() {
        let dir = tempdir().unwrap();
        let path = dir.path().join("SUBSTRATE_BENCHMARKS.md");
        let summaries: Vec<_> = SUITE_ORDER.iter().map(|id| pass_summary(id)).collect();
        regenerate_substrate_benchmarks_md(&path, &summaries).unwrap();

        let body = std::fs::read_to_string(&path).unwrap();
        assert!(body.starts_with("# memd Substrate Benchmarks"));
        for id in SUITE_ORDER {
            let open = format!("<!-- substrate-agg:{id}:open -->");
            let close = format!("<!-- substrate-agg:{id}:close -->");
            assert!(body.contains(&open), "missing open marker for {id}");
            assert!(body.contains(&close), "missing close marker for {id}");
        }
        assert!(body.contains("## Composite"));
        assert!(body.contains("## History"));
    }

    #[test]
    fn regenerator_replaces_blocks_in_place() {
        let dir = tempdir().unwrap();
        let path = dir.path().join("SUBSTRATE_BENCHMARKS.md");
        let summaries: Vec<_> = SUITE_ORDER.iter().map(|id| pass_summary(id)).collect();
        regenerate_substrate_benchmarks_md(&path, &summaries).unwrap();
        regenerate_substrate_benchmarks_md(&path, &summaries).unwrap();

        let body = std::fs::read_to_string(&path).unwrap();
        // Each marker pair appears exactly once.
        for id in SUITE_ORDER {
            let open = format!("<!-- substrate-agg:{id}:open -->");
            assert_eq!(body.matches(&open).count(), 1, "duplicate {id} block");
        }
        // History grows by one row per run.
        let history_rows = body.lines().filter(|l| l.starts_with("- ")).count();
        assert_eq!(history_rows, 2, "history should accrue one row per run");
    }

    #[test]
    fn skipped_suite_renders_dash_status() {
        let dir = tempdir().unwrap();
        let path = dir.path().join("SUBSTRATE_BENCHMARKS.md");
        let mut summaries: Vec<_> = SUITE_ORDER.iter().map(|id| pass_summary(id)).collect();
        summaries[2] = SuiteSummary::skipped("cross-harness", "harness unavailable");
        regenerate_substrate_benchmarks_md(&path, &summaries).unwrap();
        let body = std::fs::read_to_string(&path).unwrap();
        assert!(body.contains("**status:** skipped"));
        assert!(body.contains("harness unavailable"));
    }
}
