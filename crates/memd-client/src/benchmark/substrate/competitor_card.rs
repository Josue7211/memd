//! G5.5 — competitor card renderer.
//!
//! Produces a markdown comparison table where each cell is either a
//! numeric value (filled from a primary source) or the explicit
//! placeholder sentinel `<< PLACEHOLDER — FILL FROM PRIMARY SOURCE >>`.
//! The placeholder pattern is load-bearing: it forces a future PR to do
//! real source citation rather than inventing numbers.

use std::collections::BTreeMap;
use std::path::Path;

use crate::benchmark::substrate::aggregator::SUITE_ORDER;

pub(crate) const PLACEHOLDER: &str = "<< PLACEHOLDER — FILL FROM PRIMARY SOURCE >>";

/// One row in the competitor card.
#[derive(Debug, Clone)]
pub(crate) struct CompetitorEntry {
    pub(crate) name: String,
    /// Primary source URL or repo path. Empty string renders as PLACEHOLDER.
    pub(crate) primary_source: String,
    /// Suite_id → score. Missing keys + `None` values render as PLACEHOLDER.
    pub(crate) metrics: BTreeMap<String, Option<f64>>,
}

impl CompetitorEntry {
    pub(crate) fn unfilled(name: &str) -> Self {
        Self {
            name: name.into(),
            primary_source: String::new(),
            metrics: BTreeMap::new(),
        }
    }
}

/// Render the markdown competitor card with one row per entry. The
/// column header maps to SUITE_ORDER (canonical V5 order).
pub(crate) fn render_competitor_card(entries: &[CompetitorEntry]) -> String {
    let mut out = String::from(DOC_HEADER);
    out.push_str("| System | Source |");
    for s in SUITE_ORDER {
        out.push_str(&format!(" {s} |"));
    }
    out.push('\n');
    out.push_str("| --- | --- |");
    for _ in SUITE_ORDER {
        out.push_str(" ---:|");
    }
    out.push('\n');

    for e in entries {
        out.push_str(&format!(
            "| {} | {} |",
            e.name,
            render_source(&e.primary_source)
        ));
        for s in SUITE_ORDER {
            let cell = match e.metrics.get(*s) {
                Some(Some(v)) => format!(" {v:.3} |"),
                _ => format!(" {PLACEHOLDER} |"),
            };
            out.push_str(&cell);
        }
        out.push('\n');
    }
    out.push_str(FOOTER);
    out
}

fn render_source(s: &str) -> String {
    if s.is_empty() {
        PLACEHOLDER.into()
    } else {
        s.into()
    }
}

/// Read a fixture JSON describing one competitor entry. Returns
/// `Option<f64>` cells so unfilled metrics become PLACEHOLDER.
pub(crate) fn load_competitor_fixture(path: &Path) -> std::io::Result<CompetitorEntry> {
    let body = std::fs::read_to_string(path)?;
    let v: serde_json::Value = serde_json::from_str(&body)
        .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
    let name = v
        .get("name")
        .and_then(|x| x.as_str())
        .unwrap_or("unknown")
        .to_string();
    let primary_source = v
        .get("primary_source")
        .and_then(|x| x.as_str())
        .unwrap_or("")
        .to_string();
    let mut metrics = BTreeMap::new();
    if let Some(m) = v.get("metrics").and_then(|x| x.as_object()) {
        for (k, val) in m {
            let cell = val.as_f64();
            metrics.insert(k.clone(), cell);
        }
    }
    Ok(CompetitorEntry {
        name,
        primary_source,
        metrics,
    })
}

const DOC_HEADER: &str = "# Substrate Competitor Card\n\n\
> Compares memd's substrate benchmark numbers against named external\n\
> systems. Cells marked `<< PLACEHOLDER — FILL FROM PRIMARY SOURCE >>`\n\
> MUST be filled from a primary source (paper, official repo, public\n\
> leaderboard) before publication. Inventing numbers is a contract\n\
> violation; opening a PR with placeholders is not.\n\n";

const FOOTER: &str = "\n## Methodology\n\n\
- memd row sourced from `docs/verification/SUBSTRATE_BENCHMARKS.md`\n\
  regenerated by `memd benchmark substrate --all --regenerate-report`.\n\
- Competitor rows: cite the primary source (DOI, paper PDF, official\n\
  repo SHA, or public leaderboard URL). No numbers from blog posts,\n\
  marketing pages, or third-party summaries.\n\
- Keep the suite columns aligned to V5-INTEGRATION §5b.2 SUITE_ORDER.\n";

#[cfg(test)]
mod tests {
    use super::*;

    fn metrics(values: &[(&str, Option<f64>)]) -> BTreeMap<String, Option<f64>> {
        values.iter().map(|(k, v)| (k.to_string(), *v)).collect()
    }

    /// One literal sentinel occurs in the prose header for reader context;
    /// every cell occurrence is on top of that.
    const HEADER_OCCURRENCES: usize = 1;

    #[test]
    fn render_placeholder_when_metrics_empty() {
        let entry = CompetitorEntry::unfilled("MemPalace");
        let body = render_competitor_card(&[entry]);
        assert!(body.contains("| MemPalace |"));
        // Source column + 7 suite cells all placeholder, plus the header.
        let placeholder_count = body.matches(PLACEHOLDER).count();
        assert_eq!(
            placeholder_count,
            HEADER_OCCURRENCES + 1 + SUITE_ORDER.len()
        );
    }

    #[test]
    fn render_numeric_when_metrics_supplied() {
        let entry = CompetitorEntry {
            name: "memd".into(),
            primary_source: "docs/verification/SUBSTRATE_BENCHMARKS.md".into(),
            metrics: metrics(&[
                ("cross-session-recall", Some(1.000)),
                ("correction-propagation", Some(1.000)),
                ("cross-harness", Some(1.000)),
                ("progressive-depth", Some(1.000)),
                ("provenance-integrity", Some(1.000)),
                ("typed-retrieval", Some(1.000)),
                ("adversarial-noise", Some(1.000)),
            ]),
        };
        let body = render_competitor_card(&[entry]);
        // No PLACEHOLDER in any cell — only the explanatory header reference.
        assert_eq!(body.matches(PLACEHOLDER).count(), HEADER_OCCURRENCES);
        assert_eq!(body.matches("1.000").count(), 7);
        assert!(body.contains("docs/verification/SUBSTRATE_BENCHMARKS.md"));
    }

    #[test]
    fn render_mixes_filled_and_placeholder() {
        let entry = CompetitorEntry {
            name: "Frontier-X".into(),
            primary_source: "https://example/paper.pdf".into(),
            metrics: metrics(&[
                ("cross-session-recall", Some(0.93)),
                ("provenance-integrity", None),
            ]),
        };
        let body = render_competitor_card(&[entry]);
        assert!(body.contains("0.930"));
        // 6 unfilled suites (the None value + 5 missing keys) + header.
        assert_eq!(body.matches(PLACEHOLDER).count(), HEADER_OCCURRENCES + 6);
    }

    #[test]
    fn fixture_loads_and_renders() {
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("sample.json");
        std::fs::write(
            &path,
            r#"{
                "name": "MemPalace",
                "primary_source": "https://github.com/example/mempalace#v0.1.0",
                "metrics": {
                    "cross-session-recall": 0.812,
                    "provenance-integrity": null,
                    "adversarial-noise": 0.667
                }
            }"#,
        )
        .unwrap();

        let entry = load_competitor_fixture(&path).unwrap();
        assert_eq!(entry.name, "MemPalace");
        let body = render_competitor_card(&[entry]);
        assert!(body.contains("0.812"));
        assert!(body.contains("0.667"));
        // 5 cells unfilled (4 missing keys + 1 explicit null) + header.
        assert_eq!(body.matches(PLACEHOLDER).count(), HEADER_OCCURRENCES + 5);
    }
}
